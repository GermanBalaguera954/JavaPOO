
package paquete1;

public class Clase1 {
    private int edad;
    private String nombre;
    
    
    //Metodo Setters se establece la edad y nombre.
    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    //Metodo Getter se muestra la edad y nombre.
    public int getEdad() {
        return edad;
    }

    public String getNombre() {
        return nombre;
    }

    
    
}
